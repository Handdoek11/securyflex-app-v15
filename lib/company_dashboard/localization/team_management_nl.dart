  static String timeAgoFormat(Duration duration) {
    if (duration.inMinutes < 1) {
      return 'zojuist';
    } else if (duration.inMinutes < 60) {
      return '${duration.inMinutes} $minutesAgo';
    } else if (duration.inHours < 24) {
      return '${duration.inHours} $hoursAgo';
    } else {
      return '${duration.inDays} $daysAgo';
    }
  }
  
  static String distanceFormat(double distanceKm) {
