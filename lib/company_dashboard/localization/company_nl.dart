/// Dutch business validation patterns
class DutchBusinessValidation {
  /// Validate Dutch postal code format (1234AB)
  static bool isValidPostalCode(String postalCode) {
    final regex = RegExp(r'^\d{4}[A-Z]{2}$');
    return regex.hasMatch(postalCode.toUpperCase());
  }

  /// Format Dutch postal code to standard format (1234AB)
  static String formatPostalCode(String postalCode) {
    final cleaned = postalCode.replaceAll(RegExp(r'[\s\-]'), '').toUpperCase();
    if (cleaned.length == 6) {
      return '${cleaned.substring(0, 4)}${cleaned.substring(4)}';
    }
    return postalCode.toUpperCase();
  }

  /// Validate Dutch KvK number (8 digits)
  static bool isValidKvkNumber(String kvkNumber) {
    final cleaned = kvkNumber.replaceAll(RegExp(r'[\s\-\.]'), '');
    final regex = RegExp(r'^\d{8}$');
    return regex.hasMatch(cleaned);
  }

  /// Format Dutch KvK number with proper spacing (12 34 56 78)
  static String formatKvkNumber(String kvkNumber) {
    final cleaned = kvkNumber.replaceAll(RegExp(r'[\s\-\.]'), '');
    if (cleaned.length == 8) {
      return '${cleaned.substring(0, 2)} ${cleaned.substring(2, 4)} ${cleaned.substring(4, 6)} ${cleaned.substring(6, 8)}';
    }
    return kvkNumber;
  }

  /// Validate Dutch phone number format
  static bool isValidDutchPhone(String phoneNumber) {
    final cleaned = phoneNumber.replaceAll(RegExp(r'[\s\-\(\)]'), '');
    final regex = RegExp(r'^(\+31|0031|0)[1-9]\d{8}$');
    return regex.hasMatch(cleaned);
  }

  /// Format Dutch phone number to standard format
  static String formatDutchPhone(String phoneNumber) {
    final cleaned = phoneNumber.replaceAll(RegExp(r'[\s\-\(\)]'), '');

    // Mobile numbers (06)
    if (cleaned.startsWith('06') && cleaned.length == 10) {
      return '${cleaned.substring(0, 2)} ${cleaned.substring(2, 4)} ${cleaned.substring(4, 6)} ${cleaned.substring(6, 8)} ${cleaned.substring(8, 10)}';
    }

    // International mobile (+31 6)
    if (cleaned.startsWith('+316') && cleaned.length == 12) {
      return '+31 6 ${cleaned.substring(4, 6)} ${cleaned.substring(6, 8)} ${cleaned.substring(8, 10)} ${cleaned.substring(10, 12)}';
    }

    // Landline numbers (020, 010, etc.)
    if (cleaned.startsWith('0') && cleaned.length == 10) {
      return '${cleaned.substring(0, 3)} ${cleaned.substring(3, 6)} ${cleaned.substring(6, 10)}';
    }

    return phoneNumber; // Return original if no format matches
  }

  /// Validate Dutch IBAN format
  static bool isValidDutchIban(String iban) {
    final cleaned = iban.replaceAll(RegExp(r'[\s]'), '').toUpperCase();
    final regex = RegExp(r'^NL\d{2}[A-Z]{4}\d{10}$');
    return regex.hasMatch(cleaned);
  }

  /// Format Dutch IBAN with proper spacing
  static String formatDutchIban(String iban) {
    final cleaned = iban.replaceAll(RegExp(r'[\s]'), '').toUpperCase();
    if (cleaned.length == 18 && cleaned.startsWith('NL')) {
      return '${cleaned.substring(0, 4)} ${cleaned.substring(4, 8)} ${cleaned.substring(8, 12)} ${cleaned.substring(12, 16)} ${cleaned.substring(16, 18)}';
    }
    return iban.toUpperCase();
  }

  /// Validate Dutch BTW (VAT) number format
  static bool isValidBtwNumber(String btwNumber) {
    final cleaned = btwNumber.replaceAll(RegExp(r'[\s\-\.]'), '');
    final regex = RegExp(r'^NL\d{9}B\d{2}$');
    return regex.hasMatch(cleaned.toUpperCase());
  }

  /// Format Dutch BTW number
  static String formatBtwNumber(String btwNumber) {
    final cleaned = btwNumber.replaceAll(RegExp(r'[\s\-\.]'), '').toUpperCase();
    if (cleaned.length == 14 && cleaned.startsWith('NL') && cleaned.contains('B')) {
      return '${cleaned.substring(0, 2)}.${cleaned.substring(2, 5)}.${cleaned.substring(5, 8)}.${cleaned.substring(8, 9)}.${cleaned.substring(9, 12)}.${cleaned.substring(12, 14)}';
    }
    return btwNumber.toUpperCase();
  }

  /// Format Dutch currency with proper locale formatting
  static String formatEuros(double amount) {
    return '€${amount.toStringAsFixed(2).replaceAll('.', ',')}';
  }
}
